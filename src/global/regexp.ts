/* 
封装正则匹配规则
*/

// 手机号码
export const phoneReg = /^(13[0-9]|14[5|7]|15[0|1|2|3|5|6|7|8|9]|18[0|1|2|3|5|6|7|8|9])\d{8}$/;

// 电子邮件
export const emailReg = /^(\w-*\.*)+@(\w-?)+(\.\w{2,})+$/;

// 简单密码(以字母开头，长度在6~18之间，只能包含字母、数字和下划线)：
export const simplePassword = /^[a-zA-Z]\w{5,17}$/;

// 强密码(必须包含大小写字母和数字的组合，不能使用特殊字符，长度在8-10之间)：
export const strongPassword = /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z]).{8,10}$/;